package www.twilight.other;

/**
 * 这是一个简单的计算器类，用于执行基本的数学运算。
 * @author Alice
 * @version 1.0
 * @since 2025-05-03
 */
public class Calculator {

    /**
     * 计算两个整数的和。
     * @param a 第一个整数
     * @param b 第二个整数
     * @return 两数之和
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * 计算两个整数的差。
     * @param a 被减数
     * @param b 减数
     * @return 差值
     */
    public int subtract(int a, int b) {
        return a - b;
    }
}
